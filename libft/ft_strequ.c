#include "libft.h"

int	ft_strequ(char *s1, char *s2)
{
	int i;

	i = 0;
	if (!s1 || !s2)
		return (1);
	while ((s1[i] != '\0') && (s1[i] == s2[i]))
	{
		i = i + 1;
	}
	if ((s1[i] - s2[i]) == 0)
		return (1);
	else
		return (0);
}
