#include "libft.h"

int	ft_numlen(int n)
{
	int i;

	i = 0;
	if (!n)
		return (0);
	if (n < 0)
	{
		n = (n * -1);
		i++;
	}
	while (n)
	{
		i++;
		n /= 10;
	}
	return (i - 1);
}
