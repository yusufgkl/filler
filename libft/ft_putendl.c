#include "libft.h"

void	ft_putendl(char *str)
{
	int i;

	i = 0;
	if (!str)
		return ;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i = i + 1;
	}
	ft_putchar('\n');
}
